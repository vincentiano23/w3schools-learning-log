# 1. Integer (int)
age = 25
print("Age:", age, "| Type:", type(age))

# 2. Float (decimal numbers)
height = 5.9
print("Height:", height, "| Type:", type(height))

# 3. String (text)
name = "Alice"
print("Name:", name, "| Type:", type(name))

# 4. Boolean (True or False)
is_student = True
print("Is Student:", is_student, "| Type:", type(is_student))

# 5. List (ordered collection, changeable)
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits, "| Type:", type(fruits))

# 6. Tuple (ordered, unchangeable)
coordinates = (10, 20)
print("Coordinates:", coordinates, "| Type:", type(coordinates))

# 7. Dictionary (key-value pairs)
person = {"name": "Alice", "age": 25}
print("Person:", person, "| Type:", type(person))

# 8. Set (unordered collection, unique elements)
unique_numbers = {1, 2, 3, 3, 2}
print("Unique Numbers:", unique_numbers, "| Type:", type(unique_numbers))

# 9. None (null value)
nothing = None
print("Nothing:", nothing, "| Type:", type(nothing))
